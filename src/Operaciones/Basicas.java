public class Basicas {
    
    public int Sumar(int num1, int num2){
        return(num1+num2); 
    }
     public int Resta(int num1, int num2){
        return(num1-num2); 
    }
     public int Multiplicacion(int num1, int num2){
        return(num1*num2); 
    }
     public int Division(int num1, int num2){
        return(num1/num2); 
    } 

    
    
    
        
}
